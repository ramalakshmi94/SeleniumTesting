package week1.day1.hw;

public class Calculator 
{
	int result;
	public int add(int a,int b)
	{
		result=a+b;
		return result;
	}
	
	public int sub(int a,int b)
	{
		result=a-b;
		return result;
	}
	
	public int multiply(int a,int b)
	{
		result=a*b;
		return result;
	}
	
	public int div(int a,int b)
	{
		result=a/b;
		return result;
	}
	
}
